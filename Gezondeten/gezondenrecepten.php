<h1>Gezonden Recepten</h1>

<h2>1. Chili con carne</h2>

<h4>Benodigheden</h4>
<p>
    
    <li>1 el milde olijfolie</li>
    <li>300g magere rundergehakt</li>
    <li>4 el Mexicaanse bonenkruiden</li>
    <li>2 middel grote uien</li>
    <li>1 rode paprika</li>
    <li>800g kidneybonen in blik</li>
    <li>800g biologische tomatenblokjes in blik</li>
    <li>15g verse koriander</li>
    <li>200g zuivelspread light</li>
    <li>1 limoen</li>
</p>

<h4>Bereiding</h4>

<p>
    1.Verhit de olie in de hapjespan en bak het gehakt met de Mexicaanse bonenkruiden en peper in 5 min. rul. Snipper ondertussen de uien. Verwijder de steelaanzet en zaadlijsten van de paprika en snijd het vruchtvlees in kleine stukjes. Voeg de ui en paprika toe en bak nog 5 min. op middelhoog vuur.
</p>
<p>
    2. Doe ondertussen de bonen in een vergiet, spoel af onder koud stromend water en laat uitlekken. Voeg de bonen en tomatenblokjes toe aan het gehakt. Breng aan de kook en laat 5 min. zachtjes koken. Breng op smaak met peper. 
</p>
<p>
    3. Snijd de koriander fijn en meng de helft met de zuivelspread en peper. Snijd de limoen in parten. Serveer de chili con carne met de korianderspread en limoenparten. Bestrooi met de rest van de koriander.   
</p>


<h2>2. Groenteburger met krieltjes</h2>

<h4>Benodigheden</h4>
<p>
    <li>2 bosuitjes</li>
    <li>1 wortel</li>
    <li>400g broccolirijst</li>
    <li>3 el volkoren tarwemeel</li>
    <li>75g geraspte kaas 30+</li>
    <li>3 middel grote eieren</li>
    <li>1kg krieltjes</li>
    <li>4 el milde olijfolie</li>
    <li>250g cherrytomaten</li>
    <li>15gr verse bieslook</li>
    <li>2 tl appelciderazijn</li>
</p>

<h4>Bereiding</h4>

<p>
    1. Verwarm de oven voor op 200 °C. Snijd de bosui in dunne ringetjes. Rasp de wortel grof. Doe de bosui en wortel in een grote kom en meng de kaas, de broccolirijst en het tarwemeel erdoor. Breek de eieren boven de kom en meng ze met een spatel goed door. Breng het mengsel op smaak met peper.
</p>

<p>
    2. Schep met een lepel 8-10 bergjes (burgers) op een met bakpapier beklede bakplaat. Druk de bergjes goed aan. Snijd de grote krieltjes doormidden. Leg de krieltjes op de andere met bakpapier beklede bakplaat. Besprenkel met de helft van de olie, bestrooi met peper en eventueel zout en schep om. Bak de burgers en krieltjes in de oven in 25-30 min. goudgeel en knapperig. Wissel de bakplaten halverwege en schep de krieltjes dan meteen om.
</p>

<p>
    3. Snijd ondertussen de tomaten doormidden. Knip de bieslook fijn. Meng de tomaat en bieslook met de rest van de olie en de azijn tot een salade.
</p>


<h2>3. Ma­rok­kaan­se herfst­stoof met aard­ap­pel en tuin­boon­tjes</h2>

<h4>Benodigheden</h4>
<p>
    <li>2 tenen knoflook</li>
    <li>2 el arachideolie</li>
    <li>500 g hutspotgroente</li>
    <li>3 el ras el hanout</li>
    <li>500 g vastkokende aardappelen</li>
    <li>100 g tuttifrutti</li>
    <li>400 g koelverse pompoenstukjes</li>
    <li>2 groentebouillontabletten</li>
    <li>1 liter heet water</li>
    <li>300 g diepvries tuinbonen</li>
    <li>7½ g verse platte peterselie</li>
</p>

<h4>Bereiding</h4>

<p>
    1. Snijd de knoflook fijn. Verhit de olie in de braadpan en bak de hutspotgroente, knoflook en ras el hanout 10 min. op middelhoog vuur. Roer regelmatig. Boen ondertussen de aardappelen schoon en snijd in stukken van 2 cm. Snijd de tuttifrutti in stukjes van 1 cm.
</p>

<p>
    2. Voeg de aardappel, tuttifrutti, pompoen en het water toe, verkruimel de bouillonblokjes erboven en breng aan de kook. Roer door en laat met de deksel op de pan 25 min. op matig vuur stoven. Roer af en toe. Controleer of de aardappelen beetgaar zijn. Voeg vervolgens de tuinbonen toe en laat nog 5 min. stoven. Snijd ondertussen de peterselie fijn.
</p>

<p>
    3. Roer de peterselie erdoor, breng op smaak met peper en eventueel zout en serveer.
</p>



